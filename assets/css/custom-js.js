function hideshow() {
    if(document.getElementById("hide-show-hamburger-section").style.display =="flex") {
        document.getElementById("hide-show-hamburger-section").style.display ="none";
    }else{document.getElementById("hide-show-hamburger-section").style.display = "flex";            
        }    
    if(document.getElementById("navhide").style.opacity =="0") {
        document.getElementById("navhide").style.opacity ="1";
    }else{document.getElementById("navhide").style.opacity = "0";            
        }    
    
}

